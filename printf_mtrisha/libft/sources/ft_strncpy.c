#include <libft.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t tmp;

	tmp = (len > ft_strlen(src)) ? ft_strlen(src) : len;
	ft_bzero(dst, len);
	ft_memcpy(dst, src, tmp);
	return (dst);
}
